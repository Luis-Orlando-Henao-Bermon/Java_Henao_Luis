
package proyecto_dia3;

public class Cliente {
    int ID;
    String Nombre;
    String Apellido;
    String Correo;
    int Telefono;

    public Cliente(int ID, String Nombre, String Apellido, String Correo, int Telefono) {
        this.ID = ID;
        this.Nombre = Nombre;
        this.Apellido = Apellido;
        this.Correo = Correo;
        this.Telefono = Telefono;
    }
    
    
}
